#!/bin/bash

# VittoriaDB Basic Usage with cURL
# This script demonstrates basic VittoriaDB operations using HTTP API calls
# Make sure VittoriaDB is running: ./vittoriadb run

set -e  # Exit on any error

# Configuration
BASE_URL="http://localhost:8080"
COLLECTION_NAME="curl_demo"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Helper functions
print_header() {
    echo -e "\n${BLUE}$1${NC}"
    echo "=================================="
}

print_success() {
    echo -e "${GREEN}✅ $1${NC}"
}

print_error() {
    echo -e "${RED}❌ $1${NC}"
}

print_info() {
    echo -e "${YELLOW}ℹ️  $1${NC}"
}

# Check if VittoriaDB is running
check_connection() {
    print_header "1. Testing Connection"
    
    if curl -s -f "$BASE_URL/stats" > /dev/null; then
        print_success "Connected to VittoriaDB at $BASE_URL"
        
        # Get and display stats
        stats=$(curl -s "$BASE_URL/stats")
        echo "Database Stats: $stats"
    else
        print_error "Failed to connect to VittoriaDB"
        print_info "Make sure VittoriaDB is running with: ./vittoriadb run"
        exit 1
    fi
}

# Create a collection
create_collection() {
    print_header "2. Creating Collection"
    
    # Delete existing collection if it exists
    curl -s -X DELETE "$BASE_URL/collections/$COLLECTION_NAME" > /dev/null || true
    
    # Create new collection
    # Note: index_type and metric use integer values:
    # index_type: 0=flat, 1=hnsw, 2=ivf
    # metric: 0=cosine, 1=euclidean, 2=dot_product, 3=manhattan
    response=$(curl -s -w "%{http_code}" -X POST "$BASE_URL/collections" \
        -H "Content-Type: application/json" \
        -d '{
            "name": "'$COLLECTION_NAME'",
            "dimensions": 128,
            "index_type": 0,
            "metric": 0,
            "description": "cURL demo collection"
        }')
    
    http_code="${response: -3}"
    if [[ "$http_code" == "201" ]]; then
        print_success "Created collection '$COLLECTION_NAME'"
    else
        print_error "Failed to create collection (HTTP $http_code)"
        exit 1
    fi
}

# List collections
list_collections() {
    print_header "3. Listing Collections"
    
    collections=$(curl -s "$BASE_URL/collections")
    echo "Collections: $collections"
    
    # Count collections
    count=$(echo "$collections" | jq '. | length' 2>/dev/null || echo "unknown")
    print_success "Found $count collections"
}

