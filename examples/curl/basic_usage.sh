    echo -e "${BLUE}🚀 VittoriaDB cURL Basic Usage Example${NC}"
    echo "========================================"
    
    check_connection
    create_collection
    list_collections
    add_vectors
    add_batch_vectors
    perform_searches
    get_collection_info
    get_final_stats
    cleanup
    
    echo -e "\n${GREEN}🎉 Basic usage example completed successfully!${NC}"
    echo -e "\n${YELLOW}Next steps:${NC}"
    echo "- Try the RAG example: ./curl/rag_example.sh"
    echo "- Test document processing: ./curl/document_processing.sh"
    echo "- Run volume tests: ./curl/volume_test.sh"
}

# Check if jq is available (optional but recommended)
if ! command -v jq &> /dev/null; then
    print_info "jq is not installed. JSON output will be raw (install with: brew install jq)"
fi

# Run main function
main "$@"
