# Get collection info
get_collection_info() {
    print_header "7. Collection Information"
    
    collection_info=$(curl -s "$BASE_URL/collections/$COLLECTION_NAME")
    echo "Collection Info:"
    echo "$collection_info" | jq '.' 2>/dev/null || echo "$collection_info"
}

# Get final stats
get_final_stats() {
    print_header "8. Final Database Statistics"
    
    final_stats=$(curl -s "$BASE_URL/stats")
    echo "Final Stats:"
    echo "$final_stats" | jq '.' 2>/dev/null || echo "$final_stats"
    
    collections=$(echo "$final_stats" | jq '.collections' 2>/dev/null || echo "unknown")
    vectors=$(echo "$final_stats" | jq '.total_vectors' 2>/dev/null || echo "unknown")
    memory=$(echo "$final_stats" | jq '.memory_usage' 2>/dev/null || echo "unknown")
    
    print_success "Database has $collections collections with $vectors total vectors"
    print_info "Memory usage: $memory bytes"
}

# Cleanup
cleanup() {
    print_header "9. Cleanup"
    
    print_info "Deleting collection '$COLLECTION_NAME'..."
    curl -s -X DELETE "$BASE_URL/collections/$COLLECTION_NAME" > /dev/null
    print_success "Collection deleted"
}

# Main execution
